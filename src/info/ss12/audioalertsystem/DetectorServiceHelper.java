package info.ss12.audioalertsystem;

import android.content.Context;


public class DetectorServiceHelper
{
	public static RecorderThread recorder;
	public static DetectorThread detector;
	public static void startServices(Context context)
	{
		recorder = new RecorderThread();
		recorder.startRecording();
		detector = new DetectorThread(recorder, (OnSignalsDetectedListener) context);
		detector.start();
				
	}
	
	public static void stopServices()
	{
		
		recorder.stopRecording();
		detector.stopDetection();
	}
	
	public static String getNumberOfAlerts()
	{
		if(detector == null)
			return "0";
		
		return String.valueOf(detector.getTotalWhistlesDetected());
	}
}
